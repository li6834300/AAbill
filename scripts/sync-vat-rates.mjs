#!/usr/bin/env node
// 从欧委会 TEDB 同步增值税率,生成 packages/core/src/vat-rates.generated.ts。
//
// 为什么是「同步进仓库」而不是「运行时调 API」:
//   1. 算钱路径不该依赖第三方可用性 —— 上游挂了不能让结算跟着挂;
//   2. 税率若在用户结算途中变动,同一张账单会前后不一致;
//   3. 生成物入库后,每次税率变动都是一个可审查、可回滚、有 git 历史的 PR。
// 新鲜度由 vat-rates.test.ts 的「过期即失败」断言 + CI 定时任务保证。
//
// 用法:npm run sync:vat-rates   (加 --check 只检查不写文件,供 CI 用)

import { readFileSync, writeFileSync } from 'node:fs';
import prettier from 'prettier';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const SOURCE_URL =
  'https://raw.githubusercontent.com/vatnode/eu-vat-rates-data/main/data/eu-vat-rates-data.json';
const SOURCE_NAME = 'European Commission TEDB (via vatnode/eu-vat-rates-data)';

// 收录范围:EU-27 全体 + 这几个常去购物的邻国。
// 不收全部 45 国是为了让下拉列表还能用 —— 需要时把国家码加进来即可。
const EXTRA_COUNTRIES = ['GB', 'CH', 'NO', 'IS', 'LI'];

const OUT = join(
  dirname(fileURLToPath(import.meta.url)),
  '../packages/core/src/vat-rates.generated.ts',
);

/** 百分比 → 整数基点。19 → 1900,5.5 → 550。 */
const toBp = (percent) => {
  const bp = Math.round(percent * 100);
  if (!Number.isFinite(bp) || bp < 0 || bp > 10_000) {
    throw new Error(`上游税率不合理: ${percent}`);
  }
  return bp;
};

const main = async () => {
  const res = await fetch(SOURCE_URL);
  if (!res.ok) throw new Error(`拉取上游失败: HTTP ${res.status}`);
  const raw = await res.json();

  if (!/^\d{4}-\d{2}-\d{2}$/.test(raw.version ?? '')) {
    throw new Error(`上游 version 格式不对: ${raw.version}`);
  }

  const picked = Object.entries(raw.rates)
    .filter(([code, v]) => v.eu_member || EXTRA_COUNTRIES.includes(code))
    .sort(([a], [b]) => a.localeCompare(b));

  if (picked.length < 30) {
    throw new Error(`上游国家数异常偏少(${picked.length}),疑似数据损坏,已中止`);
  }

  const entries = picked.map(([code, v]) => {
    // super_reduced 也是一档真实的低税率(如卢森堡 3%),并入候选后升序排列
    const reduced = [
      ...(v.reduced ?? []),
      ...(v.super_reduced ? [v.super_reduced] : []),
    ]
      .map(toBp)
      .sort((a, b) => a - b);
    return {
      code,
      name: v.country,
      standardBp: toBp(v.standard),
      reducedBp: [...new Set(reduced)],
    };
  });

  const rawBody = `// @generated by scripts/sync-vat-rates.mjs —— 请勿手改,改了下次同步会被覆盖。
// 上游:${SOURCE_NAME}
// 抓取日期:${raw.version}
//
// 税率单位为整数基点(bp):19% = 1900,5.5% = 550。

export interface CountryVatData {
  /** 上游给出的英文国名 */
  readonly name: string;
  /** 标准税率(基点) */
  readonly standardBp: number;
  /** 该国全部低税率(含 super-reduced),升序;丹麦这类没有低税率档的为空 */
  readonly reducedBp: readonly number[];
}

export const VAT_RATES_SOURCE = {
  name: ${JSON.stringify(SOURCE_NAME)},
  url: ${JSON.stringify(SOURCE_URL)},
  version: ${JSON.stringify(raw.version)},
} as const;

export const VAT_RATES = {
${entries
  .map(
    (e) =>
      `  ${e.code}: { name: ${JSON.stringify(e.name)}, standardBp: ${e.standardBp}, reducedBp: [${e.reducedBp.join(', ')}] },`,
  )
  .join('\n')}
} as const satisfies Record<string, CountryVatData>;
`;

  // 生成物也要过 prettier(用仓库自己的配置),否则每次同步都会把 lint 弄红
  const body = await prettier.format(rawBody, {
    ...(await prettier.resolveConfig(OUT)),
    parser: 'typescript',
  });

  if (process.argv.includes('--check')) {
    const current = readFileSync(OUT, 'utf8');
    if (current !== body) {
      console.error('税率数据已过时,请运行 npm run sync:vat-rates');
      process.exit(1);
    }
    console.log(`税率数据与上游一致(${raw.version})`);
    return;
  }

  writeFileSync(OUT, body);
  console.log(`已写入 ${entries.length} 个国家的税率(上游版本 ${raw.version})`);
};

await main();
